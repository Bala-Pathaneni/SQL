(4, 40),
(1, 2),
(3,77);

CREATE TABLE C
(
    ID INT,
    C INT
);

INSERT INTO C(ID, C)
VALUES
(1, 0),
(2, 220),
(3, 0),
(4, 40),
(1, 0),
(3,63);

SELECT COALESCE(A.ID,B.ID,C.ID) AS ID, 
       COALESCE(MAX(A.A),'') A,
	   COALESCE(MAX(B.B),'') B,
	   COALESCE(MAX(C.C),'') C
FROM (SELECT ID, SUM(A) AS A FROM A GROUP BY A.ID) A
FULL JOIN (SELECT ID, SUM(B) AS B FROM B GROUP BY B.ID) B ON A.ID=B.ID
FULL JOIN (SELECT ID, SUM(C) AS C FROM C GROUP BY C.ID) C ON A.ID=C.ID
GROUP BY COALESCE(A.ID,B.ID,C.ID) ORDER BY COALESCE(A.ID,B.ID,C.ID)
